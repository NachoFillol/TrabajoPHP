		<h4>Detalles de la Nintendo 64</h4>
	<p>	El Nintendo 64 es la tercera videoconsola de sobremesa producida por Nintendo, desarrollada para suceder a la Super Nintendo y para competir con el Saturn de Sega y la PlayStation de Sony.
Incorpora en su arquitectura un procesador principal de 64 bits. El soporte de almacenamiento de los juegos es en forma de cartuchos, la mayoría de ellos con memoria interna. El uso de este tipo de almacenamiento le supuso una seria desventaja comercial frente a sus competidores, ya que encarecía los costes de producción lo que aumentaba el precio final, y además, era de una capacidad de almacenamiento menor al de un CD-ROM.</p>
	<p>	Técnicamente, la utilización de cartuchos ofrece algunas ventajas frente al formato CD-ROM, siendo esta la idea que mantenía Nintendo frente a la competencia. El cartucho tiene tiempos de acceso al sistema mucho más cortos, hace posible la inclusión de coprocesadores y otros chips dentro del cartucho y técnicas como streaming en tiempo real para mejorar las capacidades de los juegos, logrando ampliar su vida útil; y en un principio parecía que podría ser más económico por no pagar derechos ("regalías" o "royalties") por uso del formato CD-ROM, ni una unidad lectora para el mismo que sería además mucho más delicada que una ranura o slot para cartuchos. Sin embargo, la escasa capacidad del cartucho en comparación con el CD-ROM supuso ausencia casi total de cinemáticas pregrabadas, una merma en la calidad del sonido y una falta de espacio para recursos como texturas y número de escenarios.</p>
	<p>	El Nintendo 64 incluyó en sus mandos unos botones dispuestos en cruz diseñados especialmente para que el usuario tomara el control de aspectos propios de juegos basados en entornos tridimensionales (el control de la perspectiva de juego o cámaras, por ejemplo), los cuales fueron utilizados por primera vez por el juego Super Mario 64. También incluyó un stick analógico, el cual permite diferentes grados de movimiento. También tuvo función de vibración gracias al periférico Rumble Pak.</p>
	<p>	Fue descontinuada el 30 de abril de 2002 y sus últimos títulos fueron Tony Hawk's Pro Skater 3 en América y Mario Party 3 en Europa y Japón. Fue sustituida por el GameCube el 14 de noviembre de 2001 en América y el 4 de mayo de 2002 en Europa.</p>
